def exponencial(numero, potencia):
    """
    Função que retorna a potência de um número
    :param numero: Numero que desejamos exponencial
    :param potencia: Potência que irá elevar o 'numero'
    :return: Retorna o exponêncial de 'numero' por 'potencia
    """
    return numero * potencia