"""
Tipo booleano:

Certo: True, False

Errado: true, false

"""

verdadeiro = True

print(verdadeiro)

falso = False

print(falso)

print("Olha a lógica matemática como vai:")
print("\n")
print("Operador 'or' com true/false:", verdadeiro or falso)
print("\n")
print("Operador 'and' com true/false:", verdadeiro and falso)
print("\n")
print("Operador 'or'com true/true:", verdadeiro or verdadeiro)
print("\n")
print("Operador 'and'com true/true:", verdadeiro and verdadeiro)
print("\n")
print("Operador 'or'com false/false:", falso or falso)
print("\n")
print("Operador 'and'com false/false:", falso and falso)


